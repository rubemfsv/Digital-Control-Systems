    #INCLUDE <P16F84A.INC>
    
    ORG 0x00
    GOTO INICIO
    
INICIO
    CLRF PORTB
    CLRW
    CLRF PORTA
    BSF STATUS,RP0
    MOVLW 0
    MOVWF TRISA
    MOVLW 1
    MOVWF TRISB
    BCF STATUS,RP0

MAIN
    BTFSS PORTB,0
    GOTO APAGA
    GOTO ACENDE
    
APAGA
    BCF PORTA,0
    GOTO MAIN

ACENDE
    BSF PORTA,0
    GOTO MAIN
    
    END
